use anyhow::{bail, Result};
use colored::Colorize;
use indicatif::{MultiProgress, ProgressBar, ProgressStyle};

use crate::{
    config, git, github,
    state::{MergesState, Strategy},
};

pub async fn run(stacked: bool, independent: bool) -> Result<()> {
    let root = git::repo_root()?;
    let mut state = MergesState::load(&root)?;

    if state.chunks.is_empty() {
        bail!("No chunks defined. Run `merges split` first.");
    }

    // Determine strategy for this run
    let strategy = if stacked {
        Strategy::Stacked
    } else if independent {
        Strategy::Independent
    } else {
        state.strategy.clone()
    };

    println!(
        "{} Pushing {} chunk(s) as {} PRs",
        "→".blue().bold(),
        state.chunks.len().to_string().yellow(),
        format!("{}", strategy).cyan()
    );

    let token = config::github_token()?;
    let gh = github::client(&token)?;

    let current_branch = git::current_branch(&root)?;
    let mp = MultiProgress::new();
    let spinner_style = ProgressStyle::default_spinner()
        .template("{spinner} {msg}")
        .unwrap();

    let mut current_stacked_base = state.base_branch.clone();

    // First pass: Refresh status and identify active chunks
    let mut chunk_statuses = Vec::new();
    for chunk in &state.chunks {
        let mut status = chunk.status.clone();
        if let Some(pr_number) = chunk.pr_number {
            if let Ok(info) = github::get_pr_info(&gh, &state.repo_owner, &state.repo_name, pr_number).await {
                if info.is_merged {
                    status = crate::state::ChunkStatus::Merged;
                } else if info.state == "closed" {
                    status = crate::state::ChunkStatus::Closed;
                } else {
                    status = crate::state::ChunkStatus::Pending;
                }
            }
        }
        chunk_statuses.push(status);
    }

    // Update state with new statuses
    let mut state_changed = false;
    for (i, status) in chunk_statuses.iter().enumerate() {
        if state.chunks[i].status != *status {
            state.chunks[i].status = status.clone();
            state_changed = true;
        }
    }
    if state_changed {
        state.save(&root)?;
    }

    let active_indices: Vec<usize> = chunk_statuses.iter().enumerate()
        .filter(|(_, s)| **s == crate::state::ChunkStatus::Pending)
        .map(|(i, _)| i)
        .collect();

    let total_active = active_indices.len();

    if total_active == 0 {
        println!("{} No active chunks to push.", "✓".green());
        return Ok(());
    }

    for (active_idx, &i) in active_indices.iter().enumerate() {
        let chunk = state.chunks[i].clone();
        let pb = mp.add(ProgressBar::new_spinner());
        pb.set_style(spinner_style.clone());
        pb.enable_steady_tick(std::time::Duration::from_millis(80));
        pb.set_message(format!("[{}] Syncing…", chunk.name));

        // Switch to chunk branch and sync with base
        // In worktree mode, operate in the chunk's worktree dir — no branch checkout needed.
        let work_dir = if state.use_worktrees {
            git::worktree_path(&root, &chunk.branch)
        } else {
            git::checkout(&root, &chunk.branch)?;
            root.clone()
        };
        pb.set_message(format!("[{}] Rebasing onto '{}'…", chunk.name, state.base_branch));
        match strategy {
            Strategy::Stacked => git::fetch_and_rebase_stacked(&work_dir, &state.base_branch)?,
            Strategy::Independent => git::fetch_and_rebase(&work_dir, &state.base_branch)?,
        }

        // Push
        pb.set_message(format!("[{}] Pushing…", chunk.name));
        git::push_branch(&work_dir, &chunk.branch)?;

        // Determine base for this PR
        let pr_base = match strategy {
            Strategy::Stacked => current_stacked_base.clone(),
            Strategy::Independent => state.base_branch.clone(),
        };

        // Build PR body
        let files_list = chunk.files.iter().map(|f| format!("- `{}`", f)).collect::<Vec<_>>().join("\n");
        let body = format!(
            "## Chunk {}/{}: {}\n\n**Strategy:** {}\n\n### Files included\n{}\n\n---\n*Generated by [merges](https://github.com/merges-cli/merges)*",
            active_idx + 1,
            total_active,
            chunk.name,
            strategy,
            files_list
        );

        let title = {
            let body = format!("[{}/{}] {}", active_idx + 1, total_active, chunk.name);
            // Explicit commit_prefix overrides auto-detection
            match &state.commit_prefix {
                Some(p) => format!("{} {}", p, body),
                None => git::pr_title(&state.source_branch, &body),
            }
        };

        let mut pr_number_to_use: Option<u64> = None;
        let mut pr_url_to_use: Option<String> = None;

        if let Some(pr_number) = chunk.pr_number {
            // We have a PR number in our state, use it.
            pr_number_to_use = Some(pr_number);
            pr_url_to_use = chunk.pr_url.clone();
        } else {
            // No PR number in state, check GitHub for an existing PR.
            pb.set_message(format!("[{}] Checking for existing PR…", chunk.name));
            if let Some(pr_info) = github::find_pr_for_branch(&gh, &state.repo_owner, &state.repo_name, &chunk.branch).await? {
                // Found an existing PR on GitHub.
                let signature = "*Generated by [merges]";
                if pr_info.body.contains(signature) {
                    // It's a merges-managed PR, adopt it.
                    pr_number_to_use = Some(pr_info.number);
                    pr_url_to_use = Some(pr_info.url.clone());
                    pb.set_message(format!("[{}] Found existing merges-managed PR #{}…", chunk.name, pr_info.number));
                } else {
                    // It's a human-created PR; warn and skip.
                    pb.finish_with_message(format!(
                        "{} [{}] Found existing non-merges PR #{} — skipping to avoid overwriting.",
                        "⚠".yellow(),
                        chunk.name.cyan(),
                        pr_info.number
                    ));
                    continue; // Skip this chunk
                }
            }
        }

        if let Some(pr_number) = pr_number_to_use {
            // PR exists (either from state or adopted from GitHub) — update it.
            pb.set_message(format!("[{}] Updating PR #{}…", chunk.name, pr_number));
            github::update_pr(
                &gh,
                &state.repo_owner,
                &state.repo_name,
                pr_number,
                &pr_base,
                &title,
                &body,
            )
            .await?;

            if chunk.pr_number.is_none() {
                // If we recovered the PR number from GitHub, save it to our state.
                state.chunks[i].pr_number = Some(pr_number);
                state.chunks[i].pr_url = pr_url_to_use.clone();
                state.chunks[i].status = crate::state::ChunkStatus::Pending; // Mark as pending
                state.save(&root)?;
            }

            pb.finish_with_message(format!(
                "{} [{}] PR #{} updated → {}",
                "✓".green(),
                chunk.name.cyan(),
                pr_number,
                pr_url_to_use.as_deref().unwrap_or("").dimmed()
            ));
        } else {
            // No existing PR found or adopted, create a new one.
            pb.set_message(format!("[{}] Creating PR…", chunk.name));
            let (pr_number, pr_url) = github::create_pr(
                &gh,
                &state.repo_owner,
                &state.repo_name,
                &title,
                &chunk.branch,
                &pr_base,
                &body,
            )
            .await?;

            state.chunks[i].pr_number = Some(pr_number);
            state.chunks[i].pr_url = Some(pr_url.clone());
            state.chunks[i].status = crate::state::ChunkStatus::Pending; // Mark as pending
            state.strategy = strategy.clone();
            state.save(&root)?;

            pb.finish_with_message(format!(
                "{} [{}] PR #{} created → {}", // Corrected to "created"
                "✓".green(),
                chunk.name.cyan(),
                pr_number,
                pr_url.dimmed()
            ));
        }

        // Update stacked base for next chunk if applicable
        if strategy == Strategy::Stacked {
            current_stacked_base = chunk.branch.clone();
        }
    }

    // In classic mode, return to the original branch
    if !state.use_worktrees {
        git::checkout(&root, &current_branch)?;
    }

    println!("\n{} All chunks pushed successfully!", "✓".green().bold());
    println!("  Run {} to see PR status.", "merges status".bold());

    Ok(())
}
